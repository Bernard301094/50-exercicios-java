/*
Exercício 11: Escreva um programa que imprima os números de 1 a 10.
*/

package main.java.loops_for.exercicio11;

public class Numeros1a10 {

    public static void main(String[] args) {

        int limite = 10;

        for (int i = 1; i <= limite ; i++) {
            System.out.println(i);
        }
    }
}
