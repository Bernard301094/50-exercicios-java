/*
 Exercício 26: Escreva um programa que imprima os números de 10 a 1.
*/

package main.java.loops_while.exercicio26;

public class Contagem10a1 {

    public static void main(String[] args) {

        int numero = 10;

        while(numero >= 1) {
            System.out.println(numero);
            numero--;
        }
    }
}
